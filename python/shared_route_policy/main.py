# -*- mode: python; python-indent: 4 -*-
import ncs
from ncs.application import Service

class BasicServiceCallbacks(Service):
    # The create() callback is invoked inside NCS FASTMAP and
    # must always exist.
    @Service.create
    def cb_create(self, tctx, root, service, proplist):
        self.log.info('Service create(service=', service._path, ')')
        policy=root.devices.device[service.device].config.route_policy.create(service.policy)
        policy.value = ''.join(service.chunk)


def policy_if_statement(match,set):
    return (
f'''  if community matches-any ({match}) then\r
    set extcommunity rt ({set}) additive\r
  endif\r
''' )

class ServiceCallbacks(Service):

    # The create() callback is invoked inside NCS FASTMAP and
    # must always exist.
    @Service.create
    def cb_create(self, tctx, root, service, proplist):
        self.log.info('Service create(service=', service._path, ')')

        vars = ncs.template.Variables()
        if_statements = [ policy_if_statement(ms.match, ms.set) for ms in service.match_set]
        vars.add('POLICY', ''.join(if_statements))
        template = ncs.template.Template(service)
        template.apply('shared-route-policy-template', vars)

